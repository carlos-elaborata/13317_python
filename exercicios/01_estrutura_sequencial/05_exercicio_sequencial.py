"""Exercício 05.

Conversão de Metros para Centímetros
Escreva um programa que converta uma medida fornecida em metros para centímetros.
"""

# 1m = 100cm

metros = float(input("Digite uma medida em metros: "))
centimetros = metros * 100
print(f"{metros} metros é igual a {centimetros} centímetros.")
