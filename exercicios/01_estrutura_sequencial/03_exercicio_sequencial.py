"""Exercício 03.

Soma de Dois Números
Desenvolva um programa que solicite dois números ao usuário e mostre o resultado da soma
desses números.
"""

numero_1 = int(input("Digite o 1º número: "))
numero_2 = int(input("Digite o 2º número: "))

soma = numero_1 + numero_2
print(f"A soma de {numero_1} com {numero_2} resulta em {soma}.")
