"""Exercício 04.

Classificação de Letras
Elabore um programa que solicite uma letra ao usuário e verifique se ela é uma vogal ou
consoante.
"""

letra = input("Digite uma letra: ").lower()

if letra == "a":
    print("Vogal")
elif letra == "e":
    print("Vogal")
elif letra == "i":
    print("Vogal")
elif letra == "o":
    print("Vogal")
elif letra == "u":
    print("Vogal")
else:
    print("Consoante")
